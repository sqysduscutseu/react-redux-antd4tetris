export default (state = initialState, action) => {
    switch(action.type){
        case BLOCK_FALL: {
            let top = state.top + 20;
            return {...state, top}
        }
    case BLOCKS_CHANHE: {
        let blocks = action.blocks.concat();
        return {...state, blocks}
    }
    case BLOCKS_BOUND: {
        let blocksBound = action.blocksBound.filter(() => true);
        return {...state, blocksBound}
    }
    case SCREEN_STATE: {
        let blockMatrix = action.blockMatrix.filter(() => true);
        return {...state, blockMatrix}
    }
    case NEXT_BLOCK: {
        let nextBlockNo = action.nextBlockNo;
        return {...state, nextBlockNo}
    }
        default: {
            return {...state}
        }
    }
}