export default (state = initialState, action) => {
    switch(action.type){
        case CURRENT_BLOCK: {
            let currentBlockNo = action.currentBlockNo;
            return {...state, currentBlockNo}
        }
    case BLOCKS_CHANHE: {
        let blocks = action.blocks.concat();
        return {...state, blocks}
    }
    case BLOCKS_PHASE: {
        let phase = action.phase;
        return {...state, phase}
    }
    case SCREEN_STATE: {
        let blockMatrix = action.blockMatrix.concat();
        return {...state, blockMatrix}
    }
    case NEXT_BLOCK: {
        let nextBlockNo = action.nextBlockNo;
        return {...state, nextBlockNo}
    }
    case SPEED_GRADE: {
        let speed = action.speed;
        return {...state, speed}
    }
    case BUTTON_DISABLED: {
        let isButtonDisabled = !state.isButtonDisabled;
        return {...state, isButtonDisabled}
    }
        default: {
            return {...state}
        }
    }
}